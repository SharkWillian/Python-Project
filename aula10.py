"""
Os operadores + e * podem fazer além de operações matemáticas
+ = Concatena duas str
* = replica valores
"""

concatenacao = 'A' + 'B' + 'C'
concat = 'Will' + ' ' + 'Shark'
print(concatenacao)
print(concat)


a_dez_vezes = 'A' * 10
tres_vezes_luiz = 3 * 'Luiz'
tresvezeswill = 'will' * 20

print(a_dez_vezes)
print(tres_vezes_luiz)
print(tresvezeswill)