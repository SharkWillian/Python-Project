"""
- Dados booleanos (Bool - boolean)
Um booleano, em ciência da computação, 
é um tipo de dado lógico que pode ter apenas um de 
dois valores possíveis: verdadeiro (True) ou falso (False).

Lembrando que, em py, a primeira letra será sempre maiúscula, True ou False.

Existem operadores para "questionar"
como por exemplo o ==, que é um operador lógico que 
questiona se um valor é igual ao outro
"""

print(10 == 10) # Sim > True (verdadeiro)
print(type(10 == 11)) # Não > False (falso)
print(10 == 11) # Não > False (falso)