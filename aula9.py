#OPERADORES

adicao = 10 + 10
print('Adição', adicao)

subtracao = 10 - 5
print('Subtração', subtracao)

multiplicacao = 10 * 10
print('Multiplicação', multiplicacao)

divisao = 10 / 2.2 #Retorna float
print('Divisão', divisao)

divisao_inteira = 10 // 2.2 #Retorna int, ela 'trunca' o número (resume)
print('Divisão inteira', divisao_inteira)

exponenciacao = 2 ** 10 #Eleva o valor
print('Exponenciação', exponenciacao)

modulo = 55 % 2 #Resto da divisão
print('Módulo', modulo)

print(20 % 2 == 0) #20 é divisível por 2, logo é par, então será True
print(17 % 2 == 0) #17 não é, logo é impar
print(20 % 2, 17 % 2)