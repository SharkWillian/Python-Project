"""
Tipos de variáveis

int = números inteiros positivos ou negativos
float = números flutuantes

"""

print(11) #int
print(-11) #int
print(0) #int

print(1.1) #Neste caso, para utilizar casas decimais, usa-se no py o ponto e não a vírgula.
#Isso pq a vírgula é utilizada como separar de variáveis.

print(2,2)#Neste caso, temos dois números dois, do tipo int
print(2.2)#Neste caso, temos o número 2,2, do tipo float

#A função type, mostra o tipo do valor
print(type('Otávio')) #Aqui usamos uma função (de identificar o tipo do valor[type]) dentro de outra função (mostrar na tela[print])
print(type('1'))
print(type('1.5'), type(-1.1), type(-0.0))